#include<stdio.h>

int main()
{
	char h[3] = { 0, }, an[4] = { 0, };

	scanf("%s", h);

	//'+' = 43 , '-' = 45 , '0' =48
	an[0] = '4';
	an[1] = '.';
	an[2] = '0';

	an[0] -= ('F' == h[0]) ? 4 : (h[0] - 'A');

	if (h[1] == '+')
		an[2] = '3';
	if (h[1] == '-')
	{
		an[0]--;
		an[2] = '7';
	}
	printf("%s", an);
	

}
/*A+: 4.3, A0: 4.0, A-: 3.7

B+: 3.3, B0: 3.0, B-: 2.7

C+: 2.3, C0: 2.0, C-: 1.7

D+: 1.3, D0: 1.0, D-: 0.7

F: 0.0*/